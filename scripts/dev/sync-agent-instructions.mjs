import { execFileSync } from 'node:child_process'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '../..')
const manifestPath = path.join(repoRoot, '.hc', 'instructions', 'sync-manifest.json')
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))

const args = new Set(process.argv.slice(2))
const checkOnly = args.has('--check')
const stageOutputs = args.has('--stage')

let hasDiff = false

for (const entry of manifest) {
    const sourcePath = path.join(repoRoot, entry.source)
    const targetPath = path.join(repoRoot, entry.target)
    const sourceBody = readFileSync(sourcePath, 'utf8').replace(/\r\n/g, '\n').trimEnd()
    const generated = [
        '<!-- Generated by scripts/sync-agent-instructions.mjs. -->',
        `<!-- Source: ${entry.source} -->`,
        '<!-- Edit the source file in .hc, not this generated target. -->',
        '',
        sourceBody,
        '',
    ].join('\n')

    let current = ''
    try {
        current = readFileSync(targetPath, 'utf8').replace(/\r\n/g, '\n')
    } catch (error) {
        if (error?.code !== 'ENOENT') {
            throw error
        }
    }

    if (current === generated) {
        continue
    }

    hasDiff = true

    if (checkOnly) {
        console.error(`[sync-agent-instructions] out of date: ${entry.target}`)
        continue
    }

    mkdirSync(path.dirname(targetPath), { recursive: true })
    writeFileSync(targetPath, generated)
    console.log(`[sync-agent-instructions] updated ${entry.target}`)
}

if (hasDiff && stageOutputs && !checkOnly) {
    const filesToStage = new Set([manifestPath, ...manifest.flatMap((entry) => [entry.source, entry.target])])
    execFileSync('git', ['add', '--', ...filesToStage], { cwd: repoRoot, stdio: 'inherit' })
}

if (checkOnly && hasDiff) {
    process.exitCode = 1
}
