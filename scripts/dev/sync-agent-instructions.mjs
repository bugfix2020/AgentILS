#!/usr/bin/env node
/**
 * sync-agent-instructions.mjs
 *
 * 真值源 = docs/instructions/*.instructions.md + docs/skills/<name>/SKILL.md
 * （由 docs/instructions/sync-manifest.json 列出）。
 *
 * 同步动作：
 *   1) 复制源到 .github/instructions/<file>（Copilot 会读这里）
 *   2) 复制 skill 源到 .github/skills/<name>/SKILL.md 和 .agents/skills/<name>/SKILL.md
 *      （保留 frontmatter 为首段）
 *   3) 生成 .github/copilot-instructions.md（入口 stub，列出全部 instruction 文件清单）
 *   4) 生成 AGENTS.md（Codex 入口 stub，与 .github/copilot-instructions.md 同源等价，仅入口语气不同）
 *
 * 入口 stub 由本脚本生成，禁止手写 —— 这样 Copilot 与 Codex 永远看到同一份内容，
 * 不存在双源漂移。
 *
 * 命令行：
 *   pnpm run sync:instructions         直接同步并写入磁盘
 *   pnpm run sync:instructions:check   只检查是否需要同步（CI / pre-commit 用），不写入；有差异 exit 1
 *   --stage                            写入后自动 git add
 */

import { execFileSync } from 'node:child_process'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'
import { format as formatWithPrettier, resolveConfig as resolvePrettierConfig } from 'prettier'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '../..')
const manifestRel = 'docs/instructions/sync-manifest.json'
const manifestPath = path.join(repoRoot, manifestRel)
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))

const args = new Set(process.argv.slice(2))
const checkOnly = args.has('--check')
const stageOutputs = args.has('--stage')

const sourceDirRel = 'docs/instructions'
const sourceSkillsDirRel = 'docs/skills'
const targetInstructionsDirRel = '.github/instructions'
const targetSkillsDirRels = ['.github/skills', '.agents/skills', '.claude/skills']
const copilotEntryRel = '.github/copilot-instructions.md'
const agentsEntryRel = 'AGENTS.md'
const claudeEntryRel = 'CLAUDE.md'

const generatedHeader = (sourceRel) =>
    [
        '<!-- Generated by scripts/dev/sync-agent-instructions.mjs. -->',
        `<!-- Source: ${sourceRel} -->`,
        '<!-- DO NOT EDIT this generated target. Edit the source file instead. -->',
        '',
    ].join('\n')

function generatedSkill(sourceRel, sourceBody) {
    if (!sourceBody.startsWith('---\n')) return generatedHeader(sourceRel) + sourceBody + '\n'

    const frontmatterEnd = sourceBody.indexOf('\n---\n', 4)
    if (frontmatterEnd === -1) return generatedHeader(sourceRel) + sourceBody + '\n'

    const frontmatter = sourceBody.slice(0, frontmatterEnd + '\n---'.length)
    const body = sourceBody.slice(frontmatterEnd + '\n---'.length).trimStart()
    return `${frontmatter}\n${generatedHeader(sourceRel)}${body}\n`
}

const writes = []
let hasDiff = false
const prettierConfigCache = new Map()

const totalTargets = manifest.sources.length + (manifest.skills?.length ?? 0) * targetSkillsDirRels.length + 3 // copilot + agents + claude entry stubs
let processedTargets = 0

function emitProgress(targetRel) {
    processedTargets += 1
    process.stderr.write(`[${processedTargets}/${totalTargets}] sync ${targetRel}\n`)
}

async function formatGenerated(targetRel, generated) {
    if (!targetRel.endsWith('.md')) return generated
    const targetPath = path.join(repoRoot, targetRel)
    let prettierConfig = prettierConfigCache.get(targetRel)
    if (!prettierConfig) {
        prettierConfig = (await resolvePrettierConfig(targetPath)) ?? {}
        prettierConfigCache.set(targetRel, prettierConfig)
    }
    return formatWithPrettier(generated, {
        ...prettierConfig,
        filepath: targetPath,
    })
}

async function maybeWrite(targetRel, generated) {
    const formatted = await formatGenerated(targetRel, generated)
    const targetPath = path.join(repoRoot, targetRel)
    let current = ''
    try {
        current = readFileSync(targetPath, 'utf8').replace(/\r\n/g, '\n')
    } catch (error) {
        if (error?.code !== 'ENOENT') throw error
    }
    if (current === formatted) {
        emitProgress(targetRel)
        return
    }
    hasDiff = true
    if (checkOnly) {
        console.error(`[sync-agent-instructions] out of date: ${targetRel}`)
        emitProgress(targetRel)
        return
    }
    mkdirSync(path.dirname(targetPath), { recursive: true })
    writeFileSync(targetPath, formatted)
    writes.push(targetRel)
    console.log(`[sync-agent-instructions] updated ${targetRel}`)
    emitProgress(targetRel)
}

// 1) 复制每份内容源到 .github/instructions/
for (const entry of manifest.sources) {
    const sourceRel = path.posix.join(sourceDirRel, entry.file)
    const targetRel = path.posix.join(targetInstructionsDirRel, entry.file)
    const sourceBody = readFileSync(path.join(repoRoot, sourceRel), 'utf8').replace(/\r\n/g, '\n').trimEnd()
    const generated = generatedHeader(sourceRel) + sourceBody + '\n'
    await maybeWrite(targetRel, generated)
}

// 1.5) 复制 skill 源到各 agent skill 目录，但保持 YAML frontmatter 为文件首段
for (const entry of manifest.skills ?? []) {
    const sourceRel = path.posix.join(sourceSkillsDirRel, entry.dir, 'SKILL.md')
    const sourceBody = readFileSync(path.join(repoRoot, sourceRel), 'utf8').replace(/\r\n/g, '\n').trimEnd()
    const generated = generatedSkill(sourceRel, sourceBody)
    for (const targetSkillsDirRel of targetSkillsDirRels) {
        const targetRel = path.posix.join(targetSkillsDirRel, entry.dir, 'SKILL.md')
        await maybeWrite(targetRel, generated)
    }
}

// 2) 渲染入口 stub（同源等价，AGENTS.md / CLAUDE.md / copilot-instructions.md 共享同一段正文）
function renderEntryStub(audience) {
    const titleByAudience = {
        copilot: '# AgentILS Copilot Instructions',
        agents: '# AgentILS Codex / AGENTS Entry',
        claude: '# AgentILS Claude Code Entry',
    }
    const introByAudience = {
        copilot: '> 本文件由 `scripts/dev/sync-agent-instructions.mjs` 自动生成，**禁止手写**。',
        agents: '> 本文件由 `scripts/dev/sync-agent-instructions.mjs` 自动生成，**禁止手写**。\n> Codex 的入口与 Copilot **同源**，两者看到的指令完全一致。',
        claude: '> 本文件由 `scripts/dev/sync-agent-instructions.mjs` 自动生成，**禁止手写**。\n> Claude Code 的入口与 Copilot / Codex **同源**，三者看到的导读与规则真值源完全一致，仅 audience 专属规则不同。',
    }
    const title = titleByAudience[audience]
    const intro = introByAudience[audience]
    const lead = [
        '',
        '请按以下顺序阅读：',
        '',
        '1. 仓库根 [`README.md`](README.md) — 项目总览（Plan C / V1）',
        '2. `.github/instructions/` 下全部 instruction 文件（即下方列表，与本入口同源生成）：',
        '',
    ]
    const list = manifest.sources.map(
        (s) => `   - [\`.github/instructions/${s.file}\`](.github/instructions/${s.file}) — ${s.summary}`,
    )
    const tail = [
        '',
        '不要假设入口 stub 包含规则细节；规则的真值源是 `docs/instructions/*.instructions.md`，sync 脚本把它们复制到 `.github/instructions/` 供 Copilot/Codex/Claude Code 读取。',
        '',
        '调试工作区运行态文件由 `scripts/prepare-debug-workspace.cjs` 生成；不要手写或提交 `apps/vscode-debug/.vscode/mcp.json`、`WELCOME.md`、`.github/**`、`.vscode/settings.json` 的本机生成副本。',
        '',
    ]
    const audienceNoteByAudience = {
        copilot: [
            '## Copilot 专属规则',
            '',
            '- 在 VS Code 中通过命令 `agentils.openPanel` 打开 AgentILS WebView；WebView 是主要输入输出界面，Copilot chat 输出保持最小化。',
            '- 要直接调 AgentILS LM tool，在 chat 里用 `#agentilsRequestUserClarification` 等 `toolReferenceName`（package.json `contributes.languageModelTools` 列出全部）；**没有** chat participant，**没有** slash command。',
            '- 通过 AgentILS WebView 的 finish 操作结束会话，除非用户明确要求其他方式。',
            '- 项目通用规则（单向数据流、test-first、命令默认仓库根、不全仓扫描等）请直接读 `agentils.instructions.md` 的 Cardinal Rules，不要在此重复。',
            '',
        ].join('\n'),
        agents: [
            '## Codex / AGENTS 专属规则',
            '',
            '- Codex 当前没有 AgentILS 专属的运行时入口；项目通用规则全部走 `agentils.instructions.md` Cardinal Rules + 各子包 instruction。',
            '- 如未来引入 Codex 专属命令、quickfix、或 IDE 集成，再把对应入口写到这里；不要把通用规则下沉到此处（会与其它 audience 漂移）。',
            '',
        ].join('\n'),
        claude: [
            '## Claude Code 专属规则',
            '',
            '- 非平凡改动先 `EnterPlanMode` 与用户对齐方案后再动代码；一行修补 / 纯研究 / 解释代码不需要进入 plan 模式。',
            '- 跨模块探索用 Explore subagent 并行最多 3 个；目标已知（具体文件 / 符号）时直接用 Read / Grep / Glob，避免子代理空跑。',
            '- 通过 Skill 工具调用本仓库已注册的 skills（在 `.claude/skills/` 下，与 `.github/skills/` `.agents/skills/` 同源生成）。',
            '- 项目通用规则（单向数据流、test-first、命令默认仓库根、不全仓扫描等）请直接读 `agentils.instructions.md` 的 Cardinal Rules，不要在此重复。',
            '- 修改任何 instructions / skills 必须改 `docs/instructions/` 或 `docs/skills/` 源 + 跑 `pnpm run sync:instructions`；**禁止手写 `CLAUDE.md` 入口或 `.claude/skills/` 下的任何 SKILL.md**（这两者都是 sync 产物）。',
            '',
        ].join('\n'),
    }
    const audienceNote = audienceNoteByAudience[audience]
    return [title, '', intro, ...lead, ...list, ...tail, audienceNote].join('\n').replace(/\n{3,}/g, '\n\n')
}

const copilotStubBody = renderEntryStub('copilot')
const agentsStubBody = renderEntryStub('agents')
const claudeStubBody = renderEntryStub('claude')

await maybeWrite(copilotEntryRel, generatedHeader(manifestRel) + copilotStubBody.trimEnd() + '\n')
await maybeWrite(agentsEntryRel, generatedHeader(manifestRel) + agentsStubBody.trimEnd() + '\n')
await maybeWrite(claudeEntryRel, generatedHeader(manifestRel) + claudeStubBody.trimEnd() + '\n')

if (hasDiff && stageOutputs && !checkOnly) {
    const filesToStage = new Set([
        manifestRel,
        ...manifest.sources.map((s) => path.posix.join(sourceDirRel, s.file)),
        ...(manifest.skills ?? []).map((s) => path.posix.join(sourceSkillsDirRel, s.dir, 'SKILL.md')),
        ...writes,
    ])
    execFileSync('git', ['add', '--', ...filesToStage], { cwd: repoRoot, stdio: 'inherit' })
}

if (checkOnly && hasDiff) {
    process.exitCode = 1
}
