#!/usr/bin/env node
/**
 * sync-agent-instructions.mjs
 *
 * 真值源 = docs/instructions/*.instructions.md（由 docs/instructions/sync-manifest.json 列出）。
 *
 * 同步动作：
 *   1) 复制源到 .github/instructions/<file>（Copilot 会读这里）
 *   2) 生成 .github/copilot-instructions.md（入口 stub，列出全部 instruction 文件清单）
 *   3) 生成 AGENTS.md（Codex 入口 stub，与 .github/copilot-instructions.md 同源等价，仅入口语气不同）
 *
 * 入口 stub 由本脚本生成，禁止手写 —— 这样 Copilot 与 Codex 永远看到同一份内容，
 * 不存在双源漂移。
 *
 * 命令行：
 *   pnpm run sync:instructions         直接同步并写入磁盘
 *   pnpm run sync:instructions:check   只检查是否需要同步（CI / pre-commit 用），不写入；有差异 exit 1
 *   --stage                            写入后自动 git add
 */

import { execFileSync } from 'node:child_process'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '../..')
const manifestRel = 'docs/instructions/sync-manifest.json'
const manifestPath = path.join(repoRoot, manifestRel)
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))

const args = new Set(process.argv.slice(2))
const checkOnly = args.has('--check')
const stageOutputs = args.has('--stage')

const sourceDirRel = 'docs/instructions'
const targetInstructionsDirRel = '.github/instructions'
const copilotEntryRel = '.github/copilot-instructions.md'
const agentsEntryRel = 'AGENTS.md'

const generatedHeader = (sourceRel) =>
    [
        '<!-- Generated by scripts/dev/sync-agent-instructions.mjs. -->',
        `<!-- Source: ${sourceRel} -->`,
        '<!-- DO NOT EDIT this generated target. Edit the source file instead. -->',
        '',
    ].join('\n')

const writes = []
let hasDiff = false

function maybeWrite(targetRel, generated) {
    const targetPath = path.join(repoRoot, targetRel)
    let current = ''
    try {
        current = readFileSync(targetPath, 'utf8').replace(/\r\n/g, '\n')
    } catch (error) {
        if (error?.code !== 'ENOENT') throw error
    }
    if (current === generated) return
    hasDiff = true
    if (checkOnly) {
        console.error(`[sync-agent-instructions] out of date: ${targetRel}`)
        return
    }
    mkdirSync(path.dirname(targetPath), { recursive: true })
    writeFileSync(targetPath, generated)
    writes.push(targetRel)
    console.log(`[sync-agent-instructions] updated ${targetRel}`)
}

// 1) 复制每份内容源到 .github/instructions/
for (const entry of manifest.sources) {
    const sourceRel = path.posix.join(sourceDirRel, entry.file)
    const targetRel = path.posix.join(targetInstructionsDirRel, entry.file)
    const sourceBody = readFileSync(path.join(repoRoot, sourceRel), 'utf8').replace(/\r\n/g, '\n').trimEnd()
    const generated = generatedHeader(sourceRel) + sourceBody + '\n'
    maybeWrite(targetRel, generated)
}

// 2) 渲染入口 stub（同源等价，AGENTS.md 与 copilot-instructions.md 共享同一段正文）
function renderEntryStub(audience) {
    const isCopilot = audience === 'copilot'
    const title = isCopilot ? '# AgentILS Copilot Instructions' : '# AgentILS Codex / AGENTS Entry'
    const intro = isCopilot
        ? '> 本文件由 `scripts/dev/sync-agent-instructions.mjs` 自动生成，**禁止手写**。'
        : '> 本文件由 `scripts/dev/sync-agent-instructions.mjs` 自动生成，**禁止手写**。\n> Codex 的入口与 Copilot **同源**，两者看到的指令完全一致。'
    const lead = [
        '',
        '请按以下顺序阅读：',
        '',
        '1. 仓库根 [`README.md`](README.md) — 项目总览（Plan C / V1）',
        '2. `.github/instructions/` 下全部 instruction 文件（即下方列表，与本入口同源生成）：',
        '',
    ]
    const list = manifest.sources.map((s) => `   - [\`.github/instructions/${s.file}\`](.github/instructions/${s.file}) — ${s.summary}`)
    const tail = [
        '',
        '不要假设入口 stub 包含规则细节；规则的真值源是 `docs/instructions/*.instructions.md`，sync 脚本把它们复制到 `.github/instructions/` 供 Copilot/Codex 读取。',
        '',
    ]
    const audienceNote = isCopilot
        ? [
              '## Copilot 专属规则',
              '',
              '- 在 VS Code 中使用 `@agentils` 启动 AgentILS WebView 会话；用 `/runtask` 作为 prompt 入口。',
              '- WebView 启动后，WebView 是主要的输入输出界面；Copilot chat 输出保持最小化。',
              '- 仅通过 AgentILS WebView 的 finish 操作结束会话，除非用户明确要求其他方式。',
              '- `run_task_loop.next.action` 是真值：`recall_tool` → 立即再调；`await_webview` → 挂起等待；`return_control` → 任务终态返回控制权。',
              '- 优先使用 AgentILS tools 与 WebView 交互面板，不要靠纯文本澄清或无关扩展工具。',
              '- 只问继续当前任务所需的最小阻塞性澄清。',
              '',
          ].join('\n')
        : [
              '## Codex / AGENTS 专属规则',
              '',
              '- 不要一开始就全仓扫描；按当前调用链与模块边界工作。',
              '- 遵守类 React 单向数据流：派生状态只有一个真值源（`packages/mcp` `memory-store`），其他模块禁止重复计算。',
              '- 命令默认在仓库根执行；进入子目录的命令请先 `cd`，避免拼出 `packages/mcp/packages/mcp/...` 这类错误路径。',
              '- 测试先行：先定义 schema 与 I/O 合同，再写实现。',
              '',
          ].join('\n')
    return [title, '', intro, ...lead, ...list, ...tail, audienceNote].join('\n').replace(/\n{3,}/g, '\n\n')
}

const copilotStubBody = renderEntryStub('copilot')
const agentsStubBody = renderEntryStub('agents')

maybeWrite(
    copilotEntryRel,
    generatedHeader(manifestRel) +
        copilotStubBody.trimEnd() +
        '\n',
)
maybeWrite(
    agentsEntryRel,
    generatedHeader(manifestRel) +
        agentsStubBody.trimEnd() +
        '\n',
)

if (hasDiff && stageOutputs && !checkOnly) {
    const filesToStage = new Set([
        manifestRel,
        ...manifest.sources.map((s) => path.posix.join(sourceDirRel, s.file)),
        ...writes,
    ])
    execFileSync('git', ['add', '--', ...filesToStage], { cwd: repoRoot, stdio: 'inherit' })
}

if (checkOnly && hasDiff) {
    process.exitCode = 1
}
