import fs from 'node:fs'
import path from 'node:path'

import { summarizeRegistry } from './engine.mjs'
import { repoRoot } from './lib.mjs'
import { RULE_CATEGORIES, RULE_DISCOVERY_SOURCES, RULE_REGISTRY } from './rules.mjs'

const outputPath = path.join(repoRoot, 'docs/developer/agent-rule-matrix.md')
const shouldCheck = process.argv.includes('--check')

const content = renderMatrix()

if (shouldCheck) {
    const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : ''
    if (existing !== content) {
        process.stderr.write(`[agent-hooks] rule matrix is out of date: ${path.relative(repoRoot, outputPath)}\n`)
        process.exitCode = 1
    }
} else {
    fs.writeFileSync(outputPath, content)
}

function renderMatrix() {
    const summary = summarizeRegistry()
    const summaryLines = RULE_CATEGORIES.filter((category) => summary.get(category)).map(
        (category) => `- \`${category}\`: ${summary.get(category)}`,
    )

    const tableRows = RULE_REGISTRY.map(
        (rule) =>
            `| ${escapeCell(rule.id)} | ${escapeCell(rule.sourceDocument)} | ${escapeCell(rule.category)} | ${escapeCell(rule.currentType)} | ${escapeCell(rule.enforcementTarget)} | ${escapeCell(rule.currentStatus)} |`,
    )

    const sourceLines = RULE_DISCOVERY_SOURCES.map((source) => `- \`${source}\``)

    return [
        '<!-- Generated by scripts/agent-hooks/render-rule-matrix.mjs. -->',
        '',
        '# Agent Rule Matrix',
        '',
        '本表从 `scripts/agent-hooks/rules.mjs` 生成，用来把仓库中已存在的 guidance 分类为 guidance、runtime enforcement、repository enforcement 或 manual review。',
        '',
        '## Rule Discovery Sources',
        '',
        ...sourceLines,
        '',
        '## Category Summary',
        '',
        ...summaryLines,
        '',
        '## Matrix',
        '',
        '| Rule ID | Source | Category | Current Type | Enforcement Target | Status |',
        '| --- | --- | --- | --- | --- | --- |',
        ...tableRows,
        '',
    ].join('\n')
}

function escapeCell(value) {
    return String(value ?? '').replace(/\|/g, '\\|')
}
